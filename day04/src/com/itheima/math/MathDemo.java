package com.itheima.math;

public class MathDemo {
    public static void main(String[] args) {
        System.out.println(Math.abs(-56));
        System.out.println(Math.ceil(13.2));
        System.out.println(Math.ceil(13.9));
        System.out.println(Math.floor(12.3));
        System.out.println(Math.floor(12.9));
        System.out.println(Math.round(12.3));
        System.out.println(Math.round(12.9));
        System.out.println(Math.max(25,61));
        System.out.println(Math.min(25,62));
    }
}
